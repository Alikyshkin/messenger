/// Версия приложения
///
/// При каждом изменении функциональности необходимо увеличивать версию:
/// - PATCH (1.0.0 -> 1.0.1): исправления багов, мелкие изменения
/// - MINOR (1.0.0 -> 1.1.0): новые функции, обратно совместимые изменения
/// - MAJOR (1.0.0 -> 2.0.0): критические изменения, несовместимые изменения API
///
/// Также необходимо обновить версию в `pubspec.yaml`:
/// ```yaml
/// version: 1.0.1+1
/// ```
/// где первая часть (1.0.1) - версия, вторая часть (+1) - build number
class AppVersion {
  /// Текущая версия приложения
  ///
  /// Формат: MAJOR.MINOR.PATCH (например, 1.0.0)
  ///
  /// ВАЖНО: При изменении версии здесь также обновите:
  /// 1. `client/pubspec.yaml` - строка `version: X.Y.Z+N`
  /// 2. Версию на сервере (если используется для проверки обновлений)
  static const String version = '1.0.0';

  /// Build number (номер сборки)
  ///
  /// Увеличивается при каждой сборке
  static const int buildNumber = 1;

  /// Полная версия с build number
  static String get fullVersion => '$version+$buildNumber';

  /// Версия для отображения (без build number)
  static String get displayVersion => version;
}
